score = float(input("Score : "))
if score < 50 :
    print("Grade 0")
elif score < 55 :
    print("Grade 1")
elif score < 60 :
    print("Grade 1.5")
elif score < 65 :
    print("Grade 2")
elif score < 70 :
    print("Grade 2.5")
elif score < 75 :
    print("Grade 3")
elif score < 80 :
    print("Grade 3.5")
else :
    print("Grade 4")